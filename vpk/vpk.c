//  vpk search  (stub — would query registry)
// ─────────────────────────────────────────────

int vpk_search(const char *query) {
    printf("Searching registry for '%s'...\n\n", query);

    // Simulated registry results
    typedef struct { const char *name; const char *ver; const char *desc; } Pkg;
    Pkg catalog[] = {
        {"tensor.linalg",  "2.1.0", "Linear algebra for Taipan tensors"},
        {"tensor.nn",      "1.0.3", "Neural network layers and ops"},
        {"std.http",       "1.2.0", "HTTP client and server"},
        {"std.json",       "1.1.0", "JSON parse and serialize"},
        {"std.crypto",     "0.9.0", "Cryptographic primitives"},
        {"game.physics",   "0.5.0", "2D/3D physics engine"},
        {"game.render",    "0.4.0", "Rendering pipeline"},
        {"util.csv",       "1.0.0", "CSV parsing utilities"},
        {"util.regex",     "0.8.0", "Regular expressions"},
        {"sys.kernel",     "0.2.0", "Low-level kernel interface"},
    };

    int found = 0;
    printf("  %-28s %-8s %s\n", "Package", "Version", "Description");
    printf("  %-28s %-8s %s\n", "───────────────────────────", "───────", "──────────────────────────");
    for (int i = 0; i < (int)(sizeof(catalog)/sizeof(catalog[0])); i++) {
        if (!query || strstr(catalog[i].name, query) || strstr(catalog[i].desc, query)) {
            printf("  %-28s %-8s %s\n", catalog[i].name, catalog[i].ver, catalog[i].desc);
            found++;
        }
    }
    if (found == 0) printf("  No packages found for '%s'\n", query);
    else printf("\n%d package(s) found. Run 'vpk install <name>' to install.\n", found);
    return 0;
}

// ─────────────────────────────────────────────
//  vpk info
// ─────────────────────────────────────────────

int vpk_info(const char *pkg_name) {
    if (!pkg_name) {
        // Show current project info
        VpkManifest m;
        if (vpk_manifest_read(VPK_MANIFEST, &m) != 0) {
            fprintf(stderr, "vpk: no %s found. Run 'vpk init'\n", VPK_MANIFEST);
            return 1;
        }
        printf("Package info:\n");
        vpk_manifest_print(&m);
        return 0;
    }

    // Show installed package info
    VpkInstalled lock[VPK_MAX_DEPS];
    int count = 0;
    vpk_lock_read(VPK_LOCKFILE, lock, &count);
    for (int i = 0; i < count; i++) {
        if (!strcmp(lock[i].name, pkg_name)) {
            printf("Package: %s\n", lock[i].name);
            printf("Version: %s\n", lock[i].version);
            printf("Path:    %s\n", lock[i].install_path);
            printf("SHA256:  %s\n", lock[i].checksum);
            return 0;
        }
    }
    fprintf(stderr, "vpk: '%s' is not installed\n", pkg_name);
    return 1;
}

