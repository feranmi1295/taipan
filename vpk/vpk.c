static int dir_exists(const char *path) {
    struct stat st;
    return stat(path, &st) == 0 && S_ISDIR(st.st_mode);
}

static int file_exists(const char *path) {
    struct stat st;
    return stat(path, &st) == 0 && S_ISREG(st.st_mode);
}

static int mkdir_p(const char *path) {
    char tmp[512];
    strncpy(tmp, path, sizeof(tmp)-1);
    for (char *p = tmp+1; *p; p++) {
        if (*p == '/') {
            *p = '\0';
            mkdir(tmp, 0755);
            *p = '/';
        }
    }
    return mkdir(tmp, 0755) == 0 || errno == EEXIST ? 0 : -1;
}

static void trim(char *s) {
    // trim leading whitespace
    int i = 0;
    while (s[i]==' '||s[i]=='\t'||s[i]=='\r'||s[i]=='\n') i++;
    if (i) memmove(s, s+i, strlen(s)-i+1);
    // trim trailing
    int len = (int)strlen(s);
    while (len>0 && (s[len-1]==' '||s[len-1]=='\t'||s[len-1]=='\r'||s[len-1]=='\n'))
        s[--len] = '\0';
}

// ─────────────────────────────────────────────
//  Manifest read/write  (simple key=value format)
//
//  taipan.pkg format:
//  ─────────────────
//  name        = mypackage
//  version     = 1.0.0
//  description = Does something cool
//  author      = Dev Name
//  license     = MIT
//  entry       = src/main.tp
//
//  [deps]
//  tensor.linalg = 2.1.0
//  std.http      = *
//
//  [dev-deps]
//  test.runner   = 1.0.0
// ─────────────────────────────────────────────

int vpk_manifest_read(const char *path, VpkManifest *m) {
    memset(m, 0, sizeof(VpkManifest));
    FILE *f = fopen(path, "r");
    if (!f) { fprintf(stderr, "vpk: cannot read '%s'\n", path); return -1; }

    char line[512];
    int section = 0; // 0=top, 1=deps, 2=dev-deps

    while (fgets(line, sizeof(line), f)) {
        trim(line);
        if (!line[0] || line[0]=='#') continue;

        if (!strcmp(line, "[deps]"))     { section=1; continue; }
        if (!strcmp(line, "[dev-deps]")) { section=2; continue; }

        // find '=' separator
        char *eq = strchr(line, '=');
        if (!eq) continue;
        *eq = '\0';
        char *key = line; trim(key);
        char *val = eq+1; trim(val);

        if (section == 0) {
            if      (!strcmp(key,"name"))        strncpy(m->name,        val, VPK_MAX_NAME-1);
            else if (!strcmp(key,"version"))     strncpy(m->version,     val, VPK_MAX_VERSION-1);
            else if (!strcmp(key,"description")) strncpy(m->description, val, 255);
            else if (!strcmp(key,"author"))      strncpy(m->author,      val, 127);
            else if (!strcmp(key,"license"))     strncpy(m->license,     val, 31);
            else if (!strcmp(key,"entry"))       strncpy(m->entry,       val, 255);
        } else if (section == 1 && m->dep_count < VPK_MAX_DEPS) {
            strncpy(m->deps[m->dep_count].name,    key, VPK_MAX_NAME-1);
            strncpy(m->deps[m->dep_count].version, val, VPK_MAX_VERSION-1);
            m->dep_count++;
        } else if (section == 2 && m->dev_dep_count < VPK_MAX_DEPS) {
            strncpy(m->dev_deps[m->dev_dep_count].name,    key, VPK_MAX_NAME-1);
            strncpy(m->dev_deps[m->dev_dep_count].version, val, VPK_MAX_VERSION-1);
            m->dev_dep_count++;
        }
    }
    fclose(f);
    return 0;
}

int vpk_manifest_write(const char *path, const VpkManifest *m) {
    FILE *f = fopen(path, "w");
    if (!f) { fprintf(stderr, "vpk: cannot write '%s'\n", path); return -1; }

    fprintf(f, "# Taipan Package Manifest\n");
    fprintf(f, "# Generated by VPK %s\n\n", VPK_VERSION);
    fprintf(f, "name        = %s\n", m->name);
    fprintf(f, "version     = %s\n", m->version);
    fprintf(f, "description = %s\n", m->description);
    fprintf(f, "author      = %s\n", m->author);
    fprintf(f, "license     = %s\n", m->license);
    fprintf(f, "entry       = %s\n", m->entry[0] ? m->entry : "src/main.tp");

    if (m->dep_count > 0) {
        fprintf(f, "\n[deps]\n");
        for (int i = 0; i < m->dep_count; i++)
            fprintf(f, "%-24s = %s\n", m->deps[i].name, m->deps[i].version);
    }
    if (m->dev_dep_count > 0) {
        fprintf(f, "\n[dev-deps]\n");
        for (int i = 0; i < m->dev_dep_count; i++)
            fprintf(f, "%-24s = %s\n", m->dev_deps[i].name, m->dev_deps[i].version);
    }
    fclose(f);
    return 0;
}

void vpk_manifest_print(const VpkManifest *m) {
    printf("  Name        : %s\n", m->name);
    printf("  Version     : %s\n", m->version);
    printf("  Description : %s\n", m->description);
    printf("  Author      : %s\n", m->author);
    printf("  License     : %s\n", m->license);
    printf("  Entry       : %s\n", m->entry);
    if (m->dep_count > 0) {
        printf("  Dependencies (%d):\n", m->dep_count);
        for (int i = 0; i < m->dep_count; i++)
            printf("    %-24s %s\n", m->deps[i].name, m->deps[i].version);
    }
    if (m->dev_dep_count > 0) {
        printf("  Dev Dependencies (%d):\n", m->dev_dep_count);
        for (int i = 0; i < m->dev_dep_count; i++)
            printf("    %-24s %s\n", m->dev_deps[i].name, m->dev_deps[i].version);
    }
}

// ─────────────────────────────────────────────
//  Lockfile  (.vpk/lock.pkg)
//  name=version|checksum|path  one per line
// ─────────────────────────────────────────────

int vpk_lock_read(const char *path, VpkInstalled *pkgs, int *count) {
    *count = 0;
    FILE *f = fopen(path, "r");
    if (!f) return 0; // lockfile may not exist yet

    char line[768];
    while (fgets(line, sizeof(line), f) && *count < VPK_MAX_DEPS) {
        trim(line);
        if (!line[0] || line[0]=='#') continue;
        VpkInstalled *p = &pkgs[*count];
        // format: name|version|checksum|path
        char *tok = strtok(line, "|");
        if (!tok) continue; strncpy(p->name,         tok, VPK_MAX_NAME-1);
        tok = strtok(NULL, "|");
        if (!tok) continue; strncpy(p->version,      tok, VPK_MAX_VERSION-1);
        tok = strtok(NULL, "|");
        if (!tok) continue; strncpy(p->checksum,     tok, 64);
        tok = strtok(NULL, "|");
        if (tok)            strncpy(p->install_path, tok, 511);
        (*count)++;
    }
    fclose(f);
    return 0;
}

int vpk_lock_write(const char *path, const VpkInstalled *pkgs, int count) {
    mkdir_p(".vpk");
    FILE *f = fopen(path, "w");
    if (!f) { fprintf(stderr, "vpk: cannot write lockfile\n"); return -1; }
    fprintf(f, "# VPK lockfile — do not edit manually\n");
    for (int i = 0; i < count; i++)
        fprintf(f, "%s|%s|%s|%s\n",
            pkgs[i].name, pkgs[i].version,
            pkgs[i].checksum, pkgs[i].install_path);
    fclose(f);
    return 0;
}

